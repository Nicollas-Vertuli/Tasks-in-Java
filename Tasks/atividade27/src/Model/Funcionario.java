package Model;

/**
 *
 * @author nicol
 */
public class Funcionario {
    private String nome;
    private int idade;
    private String formacao;

    public Funcionario(String nome, int idade, String formacao) {
        this.nome = nome;
        this.idade = idade;
        this.formacao = formacao;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getFormacao() {
        return formacao;
    }

    public void setFormacao(String formacao) {
        this.formacao = formacao;
    }
    
}
